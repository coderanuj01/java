package coreJavaAssign;

public class ArithmeticFun {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        int addition = a+b;
        int sub = a-b;
        int division = b/a;
        int multiply = a*b;
        int modulous = a%b;

        System.out.println("add = " + addition);
        System.out.println("sum = " + sub);
        System.out.println("division = " + division);
        System.out.println("multiply = " + multiply);
        System.out.println("modulus = " + modulous);
    }
}
